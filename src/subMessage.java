	
public class subMessage{
		
		int weight;
		String[][] houses;
	
		subMessage(int weight, String[][] houses){
			this.weight = weight;
			this.houses = houses;
		}

		public int getWeight() {
			return weight;
		}

		public void setWeight(int weight) {
			this.weight = weight;
		}

		public String[][] getHouses() {
			return houses;
		}

		public void setHouses(String[][] houses) {
			this.houses = houses;
		}	
		
	}
